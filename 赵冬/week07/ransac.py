import numpy as np
import scipy as sp
import scipy.linalg as sl

def ransac(data, model, n, k, t, d, debug=False, return_all = False):
    bestfit = None
    besterr = np.inf
    best_inlier_idxs = []
    for iterations in range(k):
        maybe_indexes, test_indexes = random_partition(n, data.shape[0])
        maybe_inliers = data[maybe_indexes, :]
        test_points = data[test_indexes, :]
        maybemodel = model.fit(maybe_inliers)
        test_err = model.get_error(test_points, maybemodel)
        also_indexes = test_indexes[test_err<t]
        also_inliers = data[also_indexes,:]
        if best_inlier_idxs is None or len(maybe_indexes) + len(also_indexes) > len(best_inlier_idxs):
            best_inlier_idxs = np.concatenate((maybe_indexes, also_indexes))
            betterdata = np.concatenate((maybe_inliers, also_inliers))
            bestfit = model.fit(betterdata)
    if bestfit is None:
        raise ValueError("did't meet fit acceptance criteria")
    if return_all:
        return bestfit, {'inliers': best_inlier_idxs}
    else:
        return bestfit

def random_partition(n, n_data):
    all_indexes = np.arange(n_data)
    np.random.shuffle(all_indexes)
    return all_indexes[:n],all_indexes[n:]

class LinearLeastSquareModel:
    def __init__(self, input_columns, output_columns, debug):
        self.input_columns = input_columns
        self.output_columns = output_columns
    def fit(self, data):
        A = np.vstack([data[:, i] for i in self.input_columns]).T
        B = np.vstack([data[:, i] for i in self.output_columns]).T
        x, residues, rank, s = sl.lstsq(A,B)
        return x
    def get_error(self, data, model):
        A = np.vstack([data[:, i] for i in self.input_columns]).T
        B = np.vstack([data[:, i] for i in self.output_columns]).T
        B_fit = np.dot(A, model)
        err_per_point = np.sum((B-B_fit)**2, axis=1)
        return err_per_point

